package a.basic;

public class Output {

	public static void main(String[] args) {
		//한줄을 출력하고 개행한다
		System.out.println("ㅎㅇ");
		System.out.println("println은 줄바꿈데네");
		
		//단순한 출력문
		System.out.print("print는 안뎀");
		//print에서 개행하고 싶을때는 \n(탈출 문자)을 이용한다. \\ 하면 그냥 \ 출력
		System.out.print("ㅋㅋ\n\n\n\\n");
		
	}

}
